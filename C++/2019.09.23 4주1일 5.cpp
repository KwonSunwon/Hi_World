#include <iostream>
#include "save.h"
using namespace std;

int main()
{
	// 동적메모리 할당


	int *p = new int [1'000'000];	// new로 필요한 메모리를 요청한다.
									// 요청이 서공한다면 메모리의 위치를 전달 받는다.
									// 전달받은 메모리를 포인터로 가리킨다.

	p[ 0 ] = 123;					// 마음껏 access 한다.
	p[ 99'000 ] = 333;

	delete[] p;						// 다 썻으면 시스템에 반환한다.

	save( "소스.cpp" );
}