#include <iostream>
#include <fstream>
#include "save.h"
using namespace std;

// [문제] "소스.cpp"의 글자 중 영문자를 모두 대문자로 바꿔
// "소스대문자.cpp"로 저장하라

int main()
{
	ifstream in { "소스.cpp" };
	ofstream out { "소스대문자.cpp" };

	char c;
	while( ( c = in.get() ) != EOF )
	{
		if( c > 96 && c < 123 )
			c -= 32;
		out.put( c );
	}
	
	save( "소스.cpp" );
}


